class TestMarketScopeSignal:
    """The Phase 4 envelope carries direction + rationale + optional
    quantitative layer + confidence tier + ordered evidence list."""

    def _now(self) -> datetime:
        return datetime(2026, 7, 2, 9, 0, 0, tzinfo=timezone.utc)

    def test_minimal_signal_default_confidence(self) -> None:
        """Default confidence is 'directional' — matches the Phase 1.7 stub
        when the 4.2 deterministic rules don't fire."""
        s = MarketScopeSignal(
            direction=MarketScope.WIDE_OPEN,
            rationale="n=0 in local corpus, web fallback didn't fire either",
        )
        assert s.confidence == "directional"
        assert s.quantitative is None
        assert s.evidence == []

    def test_quantitative_signal_with_full_layer(self) -> None:
        q = MarketScopeQuant(
            competitor_count=120,
            recent_3y_count=20,
            saturation_index=0.6,
        )
        ev = MarketScopeEvidence(source="corpus", company_id=42, as_of=self._now())
        s = MarketScopeSignal(
            direction=MarketScope.SATURATED,
            rationale="competitor_count=120, growth_rate=0.17 → saturated",
            confidence="quantitative",
            quantitative=q,
            evidence=[ev],
        )
        dumped = s.model_dump(mode="json")
        assert dumped["direction"] == "saturated"
        assert dumped["confidence"] == "quantitative"
        assert dumped["quantitative"]["competitor_count"] == 120
        assert dumped["evidence"][0]["source"] == "corpus"

    def test_evidence_backed_with_web_entry(self) -> None:
        ev = MarketScopeEvidence(
            source="web",
            url="https://example.com/post",
            snippet="Niche legal AI for SMBs",
            as_of=self._now(),
        )
        s = MarketScopeSignal(
            direction=MarketScope.NICHE_BUT_REAL,
            rationale="n=3 corpus + web scrape finds a real but small wedge",
            confidence="evidence_backed",
            evidence=[ev],
        )
        dumped = s.model_dump(mode="json")
        assert dumped["confidence"] == "evidence_backed"
        assert dumped["quantitative"] is None
        assert len(dumped["evidence"]) == 1
        assert dumped["evidence"][0]["source"] == "web"

    def test_confidence_accepts_all_three_values(self) -> None:
        """The Literal type covers exactly the three tiers — exhaustive test."""
        for c in ("directional", "evidence_backed", "quantitative"):
            s = MarketScopeSignal(
                direction=MarketScope.WIDE_OPEN,
                rationale="r",
                confidence=c,  # type: ignore[arg-type]
            )
            assert s.confidence == c

    def test_invalid_confidence_rejected(self) -> None:
        with pytest.raises(ValidationError) as exc:
            MarketScopeSignal(
                direction=MarketScope.WIDE_OPEN,
                rationale="r",
                confidence="guessed",  # type: ignore[arg-type]
            )
        assert "confidence" in str(exc.value)

    def test_extra_fields_forbidden(self) -> None:
        with pytest.raises(ValidationError):
            MarketScopeSignal(  # type: ignore[call-arg]
                direction=MarketScope.WIDE_OPEN,
                rationale="r",
                bonus_field="oops",
            )

    def test_evidence_list_defaults_to_empty(self) -> None:
        s = MarketScopeSignal(direction=MarketScope.WIDE_OPEN, rationale="r")
        assert s.evidence == []


