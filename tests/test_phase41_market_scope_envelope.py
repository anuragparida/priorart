class TestMarketScopeQuant:
    """The six-field quantitative layer carries its own type-level guardrails."""

    def test_minimal_valid_quant(self) -> None:
        """The spec requires competitor_count, recent_3y_count, saturation_index;
        search_volume_proxy and growth_rate default to None. category_distribution
        defaults to {} so a zero-neighborhood doesn't break the validator."""
        q = MarketScopeQuant(
            competitor_count=0,
            recent_3y_count=0,
            saturation_index=0.0,
        )
        assert q.competitor_count == 0
        assert q.recent_3y_count == 0
        assert q.category_distribution == {}
        assert q.search_volume_proxy is None
        assert q.saturation_index == 0.0
        assert q.growth_rate is None

    def test_full_quant_round_trips(self) -> None:
        """All six fields populated — exercises the full envelope."""
        q = MarketScopeQuant(
            competitor_count=200,
            recent_3y_count=80,
            category_distribution={"B2B SaaS": 60, "AI": 40, "fintech": 30, "other": 70},
            search_volume_proxy=42,
            saturation_index=1.0,
            growth_rate=0.4,
        )
        dumped = q.model_dump(mode="json")
        assert dumped == {
            "competitor_count": 200,
            "recent_3y_count": 80,
            "category_distribution": {
                "B2B SaaS": 60,
                "AI": 40,
                "fintech": 30,
                "other": 70,
            },
            "search_volume_proxy": 42,
            "saturation_index": 1.0,
            "growth_rate": 0.4,
        }

    def test_negative_competitor_count_rejected(self) -> None:
        """competitor_count has ge=0 — negative values are invalid."""
        with pytest.raises(ValidationError) as exc:
            MarketScopeQuant(
                competitor_count=-1,
                recent_3y_count=0,
                saturation_index=0.0,
            )
        assert "competitor_count" in str(exc.value)

    def test_negative_recent_3y_count_rejected(self) -> None:
        with pytest.raises(ValidationError) as exc:
            MarketScopeQuant(
                competitor_count=10,
                recent_3y_count=-1,
                saturation_index=0.05,
            )
        assert "recent_3y_count" in str(exc.value)

    def test_saturation_index_must_be_in_unit_interval(self) -> None:
        """saturation_index has ge=0, le=1 — outside [0, 1] is invalid."""
        with pytest.raises(ValidationError) as exc:
            MarketScopeQuant(
                competitor_count=10,
                recent_3y_count=2,
                saturation_index=1.5,
            )
        assert "saturation_index" in str(exc.value)

        with pytest.raises(ValidationError):
            MarketScopeQuant(
                competitor_count=10,
                recent_3y_count=2,
                saturation_index=-0.1,
            )

    def test_negative_growth_rate_rejected(self) -> None:
        """growth_rate has ge=0 — negative is invalid when explicitly set."""
        with pytest.raises(ValidationError) as exc:
            MarketScopeQuant(
                competitor_count=10,
                recent_3y_count=2,
                saturation_index=0.05,
                growth_rate=-0.1,
            )
        assert "growth_rate" in str(exc.value)

    def test_negative_search_volume_proxy_rejected(self) -> None:
        """search_volume_proxy has ge=0 when set — negative is invalid."""
        with pytest.raises(ValidationError) as exc:
            MarketScopeQuant(
                competitor_count=10,
                recent_3y_count=2,
                saturation_index=0.05,
                search_volume_proxy=-5,
            )
        assert "search_volume_proxy" in str(exc.value)

    def test_extra_fields_forbidden(self) -> None:
        """MarketScopeQuant has extra='forbid' — a misbehaving caller adding
        an unexpected field must fail loudly rather than be silently dropped."""
        with pytest.raises(ValidationError) as exc:
            MarketScopeQuant(  # type: ignore[call-arg]
                competitor_count=10,
                recent_3y_count=2,
                saturation_index=0.05,
                some_unknown_field="oops",
            )
        assert "some_unknown_field" in str(exc.value)


