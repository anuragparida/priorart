class TestMarketScopeEvidence:
    """Each evidence entry has source ∈ {corpus, web}, with source-specific
    fields. The Pydantic validator doesn't cross-check (company_id required
    for corpus, url required for web) because that's the 4.4 LLM synthesis's
    job — but we verify the field set is open."""

    def _now(self) -> datetime:
        return datetime(2026, 7, 2, 9, 0, 0, tzinfo=timezone.utc)

    def test_corpus_evidence(self) -> None:
        e = MarketScopeEvidence(
            source="corpus",
            company_id=42,
            as_of=self._now(),
        )
        dumped = e.model_dump(mode="json")
        assert dumped["source"] == "corpus"
        assert dumped["company_id"] == 42
        assert dumped["url"] is None
        assert dumped["snippet"] is None
        # Pydantic v2 emits UTC datetimes with the 'Z' suffix on JSON
        # serialisation (rather than the '+00:00' offset form).
        assert dumped["as_of"] == "2026-07-02T09:00:00Z"

    def test_web_evidence(self) -> None:
        e = MarketScopeEvidence(
            source="web",
            url="https://example.com/post",
            snippet="We raised $5M for our legal AI tool.",
            as_of=self._now(),
        )
        dumped = e.model_dump(mode="json")
        assert dumped["source"] == "web"
        assert dumped["url"] == "https://example.com/post"
        assert dumped["snippet"] == "We raised $5M for our legal AI tool."
        assert dumped["company_id"] is None

    def test_invalid_source_rejected(self) -> None:
        """source is Literal['corpus', 'web'] — anything else is rejected."""
        with pytest.raises(ValidationError) as exc:
            MarketScopeEvidence(
                source="blog",  # type: ignore[arg-type]
                as_of=self._now(),
            )
        assert "source" in str(exc.value)

    def test_as_of_required(self) -> None:
        """as_of has no default — the timestamp is the audit trail."""
        with pytest.raises(ValidationError) as exc:
            MarketScopeEvidence(source="corpus", company_id=42)  # type: ignore[call-arg]
        assert "as_of" in str(exc.value)

    def test_extra_fields_forbidden(self) -> None:
        with pytest.raises(ValidationError):
            MarketScopeEvidence(  # type: ignore[call-arg]
                source="corpus",
                company_id=42,
                as_of=self._now(),
                extra_field="oops",
            )

    def test_datetime_serialises_as_iso8601(self) -> None:
        """Pydantic v2 serialises datetime as ISO-8601 on model_dump(mode='json').
        The frontend in api.ts types as_of as string — make sure the wire
        shape matches."""
        e = MarketScopeEvidence(source="corpus", company_id=42, as_of=self._now())
        js = e.model_dump(mode="json")
        assert isinstance(js["as_of"], str)
        assert js["as_of"].startswith("2026-07-02T")


