class TestJsonSchemaGeneration:
    """The /ideas/analyze endpoint regenerates its OpenAPI schema from
    IdeaVerdict. The new field names must match the TypeScript wire types
    in src/frontend/src/lib/api.ts so the frontend can be rebuilt
    without a manual sync step."""

    def test_idea_verdict_schema_includes_market_scope_signal(self) -> None:
        schema = IdeaVerdict.model_json_schema()
        assert "market_scope_signal" in schema["properties"]

    def test_idea_verdict_schema_preserves_market_scope_enum(self) -> None:
        """The frontend reads verdict.market_scope as a MarketScope literal —
        the JSON schema must enumerate the same 4 strings."""
        schema = IdeaVerdict.model_json_schema()
        market_scope_schema = schema["properties"]["market_scope"]
        # Pydantic v2 emits enums under "$defs" for forward refs — but
        # top-level enum fields often inline. We accept either shape.
        # Just make sure all four strings appear somewhere in the schema.
        schema_text = json.dumps(schema)
        for v in ("wide_open", "crowded_but_growing", "saturated", "niche_but_real"):
            assert v in schema_text, f"missing enum value {v!r} in schema"

    def test_market_scope_quant_schema_matches_typescript(self) -> None:
        """The TypeScript MarketScopeQuant interface in
        src/frontend/src/lib/api.ts must mirror the JSON schema field names
        exactly — same keys, same casing, same nullability."""
        schema = MarketScopeQuant.model_json_schema()
        props = schema["properties"]
        assert set(props.keys()) == {
            "competitor_count",
            "recent_3y_count",
            "category_distribution",
            "search_volume_proxy",
            "saturation_index",
            "growth_rate",
        }
        # saturation_index is [0, 1].
        assert props["saturation_index"]["minimum"] == 0
        assert props["saturation_index"]["maximum"] == 1

    def test_market_scope_evidence_schema_includes_source_literal(self) -> None:
        schema = MarketScopeEvidence.model_json_schema()
        props = schema["properties"]
        assert "source" in props
        # source is a literal enum of {"corpus", "web"}; Pydantic v2
        # emits it under the anyOf-with-const shape OR a flat enum. Either
        # way, both strings must appear.
        schema_text = json.dumps(schema)
        assert "corpus" in schema_text
        assert "web" in schema_text

    def test_market_scope_signal_schema_includes_confidence_literal(self) -> None:
        schema = MarketScopeSignal.model_json_schema()
        props = schema["properties"]
        assert "confidence" in props
        schema_text = json.dumps(schema)
        for v in ("directional", "evidence_backed", "quantitative"):
            assert v in schema_text, f"missing confidence value {v!r}"


