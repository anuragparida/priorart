class TestConfidenceLiteralContract:
    """MarketScopeConfidence is a typing.Literal — exhaustively pin the
    three allowed values so a future PR adding a fourth tier has to
    consciously update this test (and the docstring + frontend)."""

    def test_three_tiers_only(self) -> None:
        # Literal["directional", "evidence_backed", "quantitative"] —
        # Python's typing.get_args surfaces the three strings.
        import typing

        tiers = typing.get_args(MarketScopeConfidence)
        assert tiers == ("directional", "evidence_backed", "quantitative")

    def test_signal_rejects_unknown_confidence(self) -> None:
        """Even with all other fields valid, an unknown confidence value
        must fail validation — the Literal type guards the contract."""
        with pytest.raises(ValidationError):
            MarketScopeSignal(
                direction=MarketScope.WIDE_OPEN,
                rationale="r",
                confidence="ai_guessed",  # type: ignore[arg-type]
            )