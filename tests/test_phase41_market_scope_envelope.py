class TestIdeaVerdictBackwardsCompat:
    """The card body's hard rule: 'No breaking changes to the IdeaVerdict
    schema. The new fields are additive with sensible defaults.' These tests
    pin the contract that Phase 1.7 / 1.8 / 1.11 / 2.x / 3.x fixtures that
    don't set market_scope_signal still parse and serialize correctly."""

    def _minimal_competitor(self) -> CompetitorVerdict:
        return CompetitorVerdict(
            company_id=42,
            name="Dioptra",
            similarity_axes=["AI legal review"],
            key_differences=["vertical-only"],
            likely_failure_modes=["distribution"],
            evidence_links=[],
            confidence=0.7,
        )

    def test_legacy_shape_without_signal_parses(self) -> None:
        """Phase 1.7 fixture: market_scope_signal is null/absent."""
        v = IdeaVerdict(
            idea="AI legal contract review",
            top_competitors=[self._minimal_competitor()],
            market_scope=MarketScope.CROWDED_BUT_GROWING,
            market_scope_rationale="3 similar YC launches, none dominant",
            supporting_evidence=["https://yc.com/dioptra"],
        )
        assert v.market_scope == MarketScope.CROWDED_BUT_GROWING
        assert v.market_scope_signal is None
        assert v.market_scope_rationale == "3 similar YC launches, none dominant"

    def test_legacy_shape_dumps_without_signal_field(self) -> None:
        """The default=None envelope must NOT appear in JSON when null —
        the schema's default Pydantic behaviour, but worth pinning so a
        later refactor doesn't accidentally start serialising null."""
        v = IdeaVerdict(
            idea="AI legal contract review",
            top_competitors=[self._minimal_competitor()],
            market_scope=MarketScope.WIDE_OPEN,
            market_scope_rationale="stub",
        )
        dumped = v.model_dump(mode="json")
        # Pydantic v2 default: include nulls unless exclude_none=True.
        # We accept either shape — the wire contract is open. But the
        # MarketScope field is preserved as the 4-direction enum string.
        assert dumped["market_scope"] == "wide_open"
        assert dumped["market_scope_rationale"] == "stub"

    def test_market_scope_enum_strings_unchanged(self) -> None:
        """The four-direction enum values are the public API contract that
        the frontend's marketScope.ts colour-coded chip keys off. Adding
        a new field MUST NOT change these strings."""
        for scope in MarketScope:
            assert scope.value in {
                "wide_open",
                "crowded_but_growing",
                "saturated",
                "niche_but_real",
            }
        # Total count: exactly 4 (no additions).
        assert len(MarketScope) == 4

    def test_market_scope_signal_optional_with_none(self) -> None:
        """Explicit None is equivalent to default — Phase 1.7 callers
        that pre-set the field to None keep working."""
        v = IdeaVerdict(
            idea="AI legal contract review",
            top_competitors=[self._minimal_competitor()],
            market_scope=MarketScope.SATURATED,
            market_scope_rationale="stub",
            market_scope_signal=None,
        )
        assert v.market_scope_signal is None

    def test_market_scope_signal_populated_round_trips(self) -> None:
        """When populated by the 4.2/4.3/4.4 pipeline, the envelope
        direction mirrors the legacy market_scope field, and the
        confidence tier is one of the three allowed values."""
        q = MarketScopeQuant(
            competitor_count=120,
            recent_3y_count=20,
            saturation_index=0.6,
        )
        sig = MarketScopeSignal(
            direction=MarketScope.SATURATED,
            rationale="competitor_count=120, growth_rate=0.17",
            confidence="quantitative",
            quantitative=q,
        )
        v = IdeaVerdict(
            idea="AI legal contract review",
            top_competitors=[self._minimal_competitor()],
            market_scope=MarketScope.SATURATED,
            market_scope_rationale="competitor_count=120, growth_rate=0.17",
            market_scope_signal=sig,
        )
        dumped = v.model_dump(mode="json")
        assert dumped["market_scope"] == "saturated"
        assert dumped["market_scope_signal"]["direction"] == "saturated"
        assert dumped["market_scope_signal"]["confidence"] == "quantitative"
        assert dumped["market_scope_signal"]["quantitative"]["competitor_count"] == 120

    def test_market_scope_signal_direction_can_differ_from_market_scope(self) -> None:
        """The card body says the two fields CAN diverge — the legacy
        ``market_scope`` enum is the Phase 1.7 LLM call's output, and
        the 4.2/4.3/4.4 pipeline MAY override it. Pydantic doesn't
        enforce consistency between them; we keep that open by design."""
        sig = MarketScopeSignal(
            direction=MarketScope.SATURATED,
            rationale="4.2 rules say saturated",
            confidence="quantitative",
        )
        v = IdeaVerdict(
            idea="X",
            top_competitors=[],
            market_scope=MarketScope.WIDE_OPEN,  # legacy LLM stub still says wide_open
            market_scope_rationale="legacy",
            market_scope_signal=sig,
        )
        assert v.market_scope == MarketScope.WIDE_OPEN
        assert v.market_scope_signal is not None
        assert v.market_scope_signal.direction == MarketScope.SATURATED


