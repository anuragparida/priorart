"""Tests for the Phase 4.1 MarketScopeQuant envelope (card t_008aef26).

Scope:
- ``MarketScopeQuant`` validates the six fields with the type-level
  guardrails (competitor_count/recent_3y_count >=0, saturation_index
  in [0,1], growth_rate >=0) — the spec's "no breaking changes to
  the IdeaVerdict schema" rule.
- ``MarketScopeEvidence`` validates source ∈ {"corpus", "web"}, and
  the cross-source field rules (company_id for corpus, url for web).
- ``MarketScopeSignal`` composes the three layers with confidence
  tied to quantitative population.
- ``IdeaVerdict`` accepts the new optional ``market_scope_signal``
  field with ``default=None`` — the Phase 1.7/1.8/1.11 fixtures
  that DON'T set the field still parse.
- The 4-direction ``MarketScope`` enum strings are unchanged
  (``wide_open / crowded_but_growing / saturated / niche_but_real``)
  so the frontend ``marketScope.ts`` colour-coded chip keeps
  working — verified by ``model_dump(mode="json")``.
- ``model_dump(mode="json")`` produces JSON-safe shapes (datetime →
  ISO-8601 string, dict preserved, optional fields nullable).
- ``OpenAPI`` schema for the new envelope is generated by Pydantic v2
  and references the same field names the TypeScript frontend
  mirrors in ``src/frontend/src/lib/api.ts`` — checked via
  ``model_json_schema()``.

What this does NOT cover:
- The deterministic direction rules (4.2). They're a separate card.
- SearXNG/Firecrawl web augmentation (4.3).
- LLM synthesis over the new envelope (4.4).
- Eval-harness metric (4.5).
- Frontend badge wiring (4.6).
- Helena review (4.7).

Hard rule from the card body:
> No breaking changes to the IdeaVerdict schema. The new fields are
> additive with sensible defaults. The 4-direction enum values
> (wide_open / crowded_but_growing / saturated / niche_but_real)
> are part of the public API contract.

These tests are pure — they don't touch the DB, the API, or any
external service.
"""

